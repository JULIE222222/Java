package Chap03;

public class Operator01 {
	public static void main(String[] args) {
		
		  int a = 10; //정수형 변수 a를 선언하고 초기화함
        double b = 2.0; //실수형 변수 b를 선언하고 초기화함
        
		  // 각 연산자로 계산한 값을 출력
		  System.out.println("a + b ="+ (a + b));
		  System.out.println("a - b ="+ (a - b));
		  System.out.println("a * b ="+ (a * b));
		  System.out.println("a / b ="+ (a / b));
		  System.out.println("a % b ="+ (a % b));
	}

}
