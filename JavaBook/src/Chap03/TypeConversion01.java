package Chap03;

public class TypeConversion01 {
	public static void main(String[] args) {
		int x = 10;
		double y = 3.0; // 실수형으로 선언하고 초기화 함

		System.out.println(x + y); // 정수와 실수를 더한 값을 출력
		System.out.println(x - y); // 정수와 실수를 뺀 값을 출력
		System.out.println(x * y); // 정수와 실수를 곱한 값을 출력
		System.out.println(x / y); // 정수와 실수를 나눈 몫의 값을 출력
	}
}
