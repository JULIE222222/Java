package Chap03;

public class Example04 {
	public static void main(String[] args) {
		
		  int x = 10; //정수형으로 x를 선언하고 초기화 함
		  System.out.println(x); //x의 값을 출력(10)
		  
		  x += 2; // 2를 더해서 x에 넣음
		  System.out.println(x); //x의 값을 출력(12)
		  
		  x *= 2; // 2를 곱해서 x에 넣음
		  System.out.println(x);//x의 값을 출력(24)
		  
		  x -= 2;//2를 빼서 x에 넣음
		  System.out.println(x);//x의 값을 출력(2)
	}

}
