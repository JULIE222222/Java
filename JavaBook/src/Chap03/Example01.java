package Chap03;

public class Example01 {

	public static void main(String[] args) {
		
		  int x = 10, y = 20; //변수 x,y를 선언하고 초기화함
		  
		  // 각 연산자로 계산한 값을 출력
		  System.out.println("x + y ="+ (x + y));
		  System.out.println("x - y ="+ (x - y));
		  System.out.println("x * y ="+ (x * y));
		  System.out.println("x / y ="+ (x / y));
		  System.out.println("x % y ="+ (x % y));
	}

}
