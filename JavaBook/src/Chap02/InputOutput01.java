package Chap02;

public class InputOutput01 {

	public static void main(String[] args) {
		
		//자료형 선언, 초기화
		String myName = "홍길동"; //문자열
		int myAge = 20; //정수
		
		//자료형 값 출력
		System.out.println("이름: " + myName + " 나이: " + myAge); //문자열대신 변수를 넣어줘도 됨, 연결해서 계속 써줄수 있음!
		System.out.println("나이: " + myAge);
	}
}
