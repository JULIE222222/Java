package Assignment02;

public class SpecialCharEx {
	
	public static void main(String[] args) {
		
		System.out.println('\''); //이스케이프 문자(\')출력
		System.out.println("abc\t123\b456"); //이스케이프 문자(\t:탭,\b:백스페이스)를 이용하여 문자열 출력
		System.out.println('\n'); //줄바꿈 출력
		System.out.println("\"Hello\""); //이스케이프 문자(\"\")를 이용하여 큰따옴표와 문자열을 출력
		System.out.println("c:\\"); // 이스케이프 문자(\\)를 이용하여 문자열 출력
		
	}

}
